class Demo{
	public static void main(String args[]){
		int result1 = -5 + 8 * 6;
		int result2 = (55 + 9) % 9;
		int result3 = 20 + -3 * 5 / 8;
		int result4 = 5 + 15 / 3 * 2 - 8 % 3;
		System.out.println("Value of -5 + 8 * 6 is : " +result1);
		System.out.println("Value of (55 + 9) % 9 is : " +result2);
		System.out.println("Value of 20 + -3 * 5 / 8 is : " +result3);
		System.out.println("Value of 5 + 15 / 3 * 2 - 8 % 3 is : " +result4);
	}
}